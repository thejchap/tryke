"""Generate a markdown summary table from hyperfine JSON results.

Usage:
    uv run python benchmarks/summarize.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

RESULTS_DIR = Path(__file__).parent / "results"
OUTPUT = Path(__file__).parent / "RESULTS.md"

_MIN_RESULTS = 2
_MICROSECOND_THRESHOLD = 0.001


def _load(name: str) -> dict | None:
    path = RESULTS_DIR / f"{name}.json"
    if not path.exists():
        return None
    with path.open() as f:
        return json.load(f)


def _fmt(seconds: float) -> str:
    if seconds < _MICROSECOND_THRESHOLD:
        return f"{seconds * 1_000_000:.0f}\u00b5s"
    if seconds < 1:
        return f"{seconds * 1000:.1f}ms"
    return f"{seconds:.2f}s"


def _ratio(a: float, b: float) -> str:
    if b == 0:
        return "\u2014"
    r = a / b
    return f"{r:.1f}x"


def main() -> None:
    lines: list[str] = []
    lines.append("# Benchmark Results\n")
    lines.append(
        "> Auto-generated by `uv run python benchmarks/summarize.py`. "
        "Do not edit manually.\n"
    )

    # discovery
    lines.append("## Discovery\n")
    lines.append("| Scale | tryke | pytest | Speedup |")
    lines.append("|-------|-------|--------|---------|")

    for scale in [50, 500, 5000]:
        disc = _load(f"discovery_{scale}")
        if disc and len(disc["results"]) >= _MIN_RESULTS:
            t = disc["results"][0]["mean"]
            p = disc["results"][1]["mean"]
            lines.append(f"| {scale} | {_fmt(t)} | {_fmt(p)} | {_ratio(p, t)} |")

    # sequential execution
    lines.append("\n## Sequential: tryke (`-j1`) vs pytest\n")
    lines.append("| Scale | tryke -j1 | pytest | Speedup |")
    lines.append("|-------|-----------|--------|---------|")

    for scale in [50, 500, 5000]:
        seq = _load(f"sequential_{scale}")
        if seq and len(seq["results"]) >= _MIN_RESULTS:
            t = seq["results"][0]["mean"]
            p = seq["results"][1]["mean"]
            lines.append(f"| {scale} | {_fmt(t)} | {_fmt(p)} | {_ratio(p, t)} |")

    # parallel execution
    lines.append("\n## Parallel: tryke vs pytest-xdist (`-nauto`)\n")
    lines.append("| Scale | tryke | pytest-xdist | Speedup |")
    lines.append("|-------|-------|-------------|---------|")

    for scale in [50, 500, 5000]:
        par = _load(f"parallel_{scale}")
        if par and len(par["results"]) >= _MIN_RESULTS:
            t = par["results"][0]["mean"]
            p = par["results"][1]["mean"]
            lines.append(f"| {scale} | {_fmt(t)} | {_fmt(p)} | {_ratio(p, t)} |")

    lines.append("")
    OUTPUT.write_text("\n".join(lines))
    sys.stdout.write(f"wrote {OUTPUT}\n")


if __name__ == "__main__":
    main()
